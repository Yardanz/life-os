type DictKey = keyof (typeof dict)["en"];

export function t(key: string, locale: Locale): string {
  const safeKey = key as DictKey;
  return dict[locale]?.[safeKey] ?? dict.en[safeKey] ?? key;
}

export type Lang = Locale;

export function normalizeLang(value: string | null | undefined): Lang | null {
  if (value === "en" || value === "ru") {
    return value;
  }
  return null;
}

export function getInitialLang(urlLang?: string | null): Lang {
  const fromUrl = normalizeLang(urlLang);
  if (fromUrl) return fromUrl;
  if (typeof window !== "undefined") {
    const storedLang = normalizeLang(window.localStorage.getItem("lifeos_lang"));
    if (storedLang) return storedLang;
    const storedLegacy = normalizeLang(window.localStorage.getItem("lifeos_locale"));
    if (storedLegacy) return storedLegacy;
  }
  return "en";
}

export function setLang(lang: Lang): void {
  if (typeof window === "undefined") return;
  window.localStorage.setItem("lifeos_lang", lang);
  window.localStorage.setItem("lifeos_locale", lang);
  const url = new URL(window.location.href);
  url.searchParams.set("lang", lang);
  window.history.replaceState(null, "", url.toString());
}
